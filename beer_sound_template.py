_cache = {}

def _decode_template(name):
    if name not in TEMPLATES:
        raise ValueError(f"Unknown template: {name}")
    encoded = TEMPLATES[name]
    compressed = base64.b64decode(encoded)
    decompressed = zlib.decompress(compressed)
    audio = np.frombuffer(decompressed, dtype=np.int16)
    return audio / 32768.0

def get_template(name):
    if name not in _cache:
        _cache[name] = _decode_template(name)
    return _cache[name]

def get_all_templates():
    return {name: get_template(name) for name in TEMPLATES.keys()}

def get_cached_template():
    return get_template("close")

def get_cached_template_far():
    return get_template("far")
